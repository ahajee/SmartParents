              <h2>Fake News</h2>
              <p>
              Fake news are stories that are false, fabricated with no variable facts, source and quotes. (Shevon & Angela, 2023). 
              In malaysia, A total of 3,285 complaints regarding content deemed to be fake news between 2020 and May 31, 2022 were received by the Malaysian Communications and Multimedia Commission (MCMC)

              </p>
              <p>
                The use of social media are on the rise from 5% in 2005 to 79% in 2019 (Ortiz-Ospina & Roser, 2019). With Facebook as the most dominant
                social media application for over a dacade, it is said that some social media sites are particularly popular among specific population groups.
                In Malaysia, seven investigation papers has been opened by the Royal Malaysian Police involving the spread of fake news, racist sentiments and racial hatred articles since Dec 1.
              </p>
              <p>
              Since the covid-19, the number of fake news and misinformation has increased a lot. Some of the top five most popular fake news topics in Malaysia are fake news pertaining to governance with 37%,
              Coming second is news related to health and crime with each recorded 14% respectively.
              Next, issues related to consumerism such as halal issues and unregistered product which recorded a total of 13%.
              Also on the top five list, MCMC said security is at 9%.
              In addition to that, MCMC has launched the sebernarnya.my portal. The portal shares suspicious information and unverified news articles.
  
            </p>

