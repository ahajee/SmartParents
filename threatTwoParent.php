              <h2>Online Scam and Fraud via Messaging App</h2>
              <p>
                Over 90,000 online fraud has been reported since 2017 to 2021 in Malaysia(BERNAMA,2021). Based on a survey on parents about cybersecurity Threats
                that often happen to them, 77.1% of the parents admit that they have faced online scam and fraud via messaging app such as WhatsApp and Telegram.

              </p>
              <p>
              Internet fraud is defined as the act of obtaining money through an online communication technology to potential victims. (Shang et al., 2023).
               These days, online fraud not only happens through email but with current technology devices such as mobile phones, it often happens through 
               social networking sites (SNS) such as social media and messaging applications. 
              With this current technology, people can disguise themselves as another person or legitimate organization to gain trust and start their scamming or fraud activities( Frauenstein and Flowerday, 2020).
              </p>
              <p>
              It was reported that older adults are most likely to me the victim of fraud activities(Carcach et al., 2001)
          Some examples of fraud and scam activities that people experienced are through messaging apps such as WhatsApp and Telegram. 
          Receiving messages from “relatives” and “friends” asking for money. Other than that, fraud in the form of phishing through email sent by
           “organizations” that seem legitimate and sometimes people also experience scam and fraud through online shopping.

              </p>
              <div class="testimonial-item">
                <p>
                  <i class="bi bi-quote quote-icon-left"></i>
                  And  do  not consume  one another's  wealth  unjustly or  send  it [in  bribery] to  the rulers in order that [they might aid] you [to] consume a 
                  portion of the wealth of the people in sin, while you know [it is unlawful]” 
                  <i class="bi bi-quote quote-icon-right"></i> -(al-Baqarah 2:188)
                </p>
                <div>
                
                </div>
              </div>

              <p>
              Even in the Quran,  fraud has been mentioned in the context where Allah has prohibited Muslim to consume others' wealth unjustly. (Siti Sara et al., 2013)
              Thus, it is our responsibility to protect ourselves from being a victim of fraud and scam by educating ourselves about it and people around us to not fall into the threats.   
            </p>

