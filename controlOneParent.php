              <h2>Protect Your Family from Unwanted Attention</h2>
              <p>
              Social media has been used by many since early 2000. As time goes by the technology has evolved and many new social media platforms have been created.
               A person might have multiple social media accounts to connect with people. 
              Social media platforms offer a place for expressing oneself, experiences, and emotions with broader online communities via posts, 
              tweets, shares, likes, and reviews.(Page et al., 2022)

              </p>
              <p>
              One of the privacy challenges in social media is  the issues of controlling access to oneself, both in terms of availability and physical access.
              People tend to share everything on social media to the point that the line between privacy of life and social media presence gets blurred.

              </p>

              <div class="testimonial-item">
                <p>
                  <i class="bi bi-quote quote-icon-left"></i>
                  He replied, “O my dear son! Do not relate your vision to your brothers, or they will devise a plot against you. Surely Satan is a sworn enemy to humankind.
                  <i class="bi bi-quote quote-icon-right"></i> -(Yusuf 12:5)
                </p>
                <div>
                 
                </div>
              </div>

              <p>
              The context of this verse was about when prophet Yusuf shared the details of his dream to his brothers which led to them feeling envy and then harm him.

              <p>
              One of the ways to protect family from unwanted attention is by imagining the audience. (Page et al., 2022). Instead of thinking that the audience are like-minded people, we should expect
               a bigger audience because the network is wide and anybody can encounter our posts. 
              </p>
              <p>
              Next, a person can change the setting to private and make sure your followers are real accounts of people you know. 
              It is better for social media users to filter contents before posting by checking if it is appropriate enough and does not expose details of private life.
            </p>

