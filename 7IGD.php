              <h2>Too Much Game Time Causing Internet Gaming Disorder</h2>
              <p>
              Do you know children are too exposed to electronic devices around them and how long your children play with the tablets in a day? 

              They are prone to these techonlogies making it 'necessary to play' even for a little while each day.
          <br><br>    

              </p>

              <p>
              When your child start to get addicted to computer, he may start to turn to video games as a way of coping with difficult life issues.
              Concerning this problem, it can lead to health issues such as Internet Gaming Disorder. So what is IGD? <br>
              IGD is a new problem included as a proposed psychiatric disorder under Section III in the Diagnostic and Statistical Manual of Mental Disorders (DSM-5) as a condition on which future research is encouraged. 
              Hence, this shows this problem is serious enough to be a part of psychiatric disorder. 
              </p>  

              <p>
            <br>  1. The behavior is when the child is preoccupied with gaming.
           <br>   2. Your child uses the behavior in order to modify the mood for example gaming is used to escape reality. 
           <br>   3. Withdrawal symptoms occur upon discontinuation of the behavior when your child feels anxious, depressed, and irritable if he/she is prevented from playing. 
            </p>

            <p>
                If your children has these criteria like these: preoccupied with games, persistent usage of Internet in games, or using the Internet to escape some stress or relieve mood, you should take some actions to prevent this from happening worse.
                A lot of bad effects your children needs to endure if they are one the IGD victims. Some of them are game addiction, their bodies tend to be sick as they are prone to back pain and obesity, and relationship failure with the family. 
                In terms of those physical and mental health issues, surely IGD is not good for your children.
              </p>

             
              

              

