              <h2>Oversharing of Family Content on Social Media</h2>
              <p>
              A report from We Are Social(WAS) in 2020 stated that 59% of the world's population are internet users and 49% are active social media users.
               For daily average time that internet users from age 16 to 64 spend using social media on any device,Malaysia was placed at number 16 among 
               other 47 countries on the ranking with two hours and twenty-four minutes(Kemp, 2022; TURGUT et al., 2021).


              </p>
              <p>
              Parents tend to share content of the family on social media for others to see. It can be a family picture or a picture of a family member,
              However, this action will not only gain attention of people they know but also strangers online. Especially social media account with public setting.
              That allow public to see user's posts.Sharing information such as childrens photos on social media not just exposing pictures of the minors but at the same time information of them. 
              This can attract cyber predators and also identity thieves( Iskül & Joamets, 2021).
              </p>

              <div class="testimonial-item">
                <p>
                  <i class="bi bi-quote quote-icon-left"></i>
                  During the first quarter of 2022, there were 93,368 IP addresses detected engaging in cyber-paedophilia activities from 2017 in Malaysia
                  <i class="bi bi-quote quote-icon-right"></i>  - (PDRM,2022)
                </p>
                <div>
                </div>
              </div>
              <p>
              It was demonstrated that photos that were shared by parents end up in child exploitation sharing sites (Staufenberg, 2016;Iskül & Joamets, 2021).
               Hence, it is an important reminder for parents to stop oversharing on social media to avoid any vulnerability and protect from danger that can happen to the family.

              </p>

            </div>
          </div>

